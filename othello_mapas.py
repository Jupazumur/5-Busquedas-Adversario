def generar_mapa_ady():
    """
    Genera un diccionario de diccionarios asociando cada casilla con su 
    casilla destino para cada dirección posible (dentro del tablero).

    mapa: Diccionario de diccionarios
    casillas_validas: Diccionario direccion-casilla
    """
    mapa = {}
    
    for casilla in range(64):
        
        casillas_validas = {}
        
        columna_actual = casilla % 8
        fila_actual = casilla // 8
        
        for direccion in DIRECCIONES:
            x, y = direccion
            nueva_columna = columna_actual + x
            nueva_fila = fila_actual - y
            
            if 0 <= nueva_columna < 8 and 0 <= nueva_fila < 8:
                nueva_casilla = nueva_fila * 8 + nueva_columna
                casillas_validas[direccion] = nueva_casilla
                
        mapa[casilla] = casillas_validas
        
    return mapa

